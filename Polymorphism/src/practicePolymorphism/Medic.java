package practicePolymorphism;

public class Medic extends Soldier {

	public void shootGun() {
		System.out.println("Medic is shooting!");
	}

	public void startRun() {
		System.out.println("Medic is running!");
	}

	public void crouchDown() {
		System.out.println("Medic is crouching down!");
	}

}
